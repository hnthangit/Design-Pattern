
public class Tinh {
	private String maTinh;
	private String tenTin;
	public String getMaTinh() {
		return maTinh;
	}
	public void setMaTinh(String maTinh) {
		this.maTinh = maTinh;
	}
	public String getTenTin() {
		return tenTin;
	}
	public void setTenTin(String tenTin) {
		this.tenTin = tenTin;
	}
	public Tinh(String maTinh, String tenTin) {
		super();
		this.maTinh = maTinh;
		this.tenTin = tenTin;
	}
	public Tinh() {
		super();
	}

	
}
